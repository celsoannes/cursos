# Título:       Exercício 3 – Somando dois números
# Desafio:      003
# Requisito:    Aula 06
# Enunciado:    Crie um programa que leia dois número e mostre a soma entre eles.

print('====== EXERCÍCIO 003 ======')
n1 = int(input('Informe o primeiro número: '))
n2 = int(input('Informe o segundo número: '))
s = n1 + n2
print('A soma entre {} mais {} é igual à: {}'.format(n1, n2, s))
