# Desafio:      006
# Enunciado:    Crie um algoritimo que leia um número e mostre o seu dobro, triplo e raiz quadrada

print('====== DESAFIO 006 ======')
n = int(input('Digite um número: '))
m = n * 2
p = n ** 3
r = n / 2
print('O dobro de {} é: {}\n O triplo de {} é: {}.\n A raiz quadrada de {} é: {}'.format(n, m, n, p, n, r))
